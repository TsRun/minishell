#include "ft_printf.h"

int	ft_atoi_printf(const char *str, int *i)
{
	int	res;

	res = 0;
	while (str[*i] >= '0' && str[*i] <= '9')
		res = res * 10 + str[(*i)++] - '0';
	return (res);
}
